class SupabaseSync {
    constructor() {
        this.supabase = null;
        this.currentUser = null;
        this.isOnline = navigator.onLine;
        this.pendingSync = [];
        
        this.init();
    }

    async init() {
        try {
            // Carrega Supabase do CDN
            await this.loadSupabaseLibrary();
            
            // Inicializa cliente
            const { createClient } = supabase;
            this.supabase = createClient(
                window.SUPABASE_CONFIG.url,
                window.SUPABASE_CONFIG.anonKey
            );

            console.log('‚úÖ Supabase conectado');
            
            // Verifica se j√° est√° logado
            this.checkExistingSession();
            
            return true;
        } catch (error) {
            console.error('‚ùå Erro ao conectar Supabase:', error);
            return false;
        }
    }

    async loadSupabaseLibrary() {
        return new Promise((resolve, reject) => {
            if (typeof supabase !== 'undefined') {
                resolve();
                return;
            }

            const script = document.createElement('script');
            script.src = 'https://unpkg.com/@supabase/supabase-js@2';
            script.onload = resolve;
            script.onerror = reject;
            document.head.appendChild(script);
        });
    }

    async checkExistingSession() {
        try {
            // MUDAN√áA: Verifica localStorage ao inv√©s de auth oficial
            const savedUser = localStorage.getItem('supabase_user');
            
            if (savedUser) {
                const userData = JSON.parse(savedUser);
                
                // Valida se o usu√°rio ainda existe no banco
                const { data: validUser } = await this.supabase
                    .from('usuarios')
                    .select('*')
                    .eq('id', userData.id)
                    .single();
                
                if (validUser) {
                    this.currentUser = validUser;
                    console.log('üë§ Usu√°rio logado:', this.currentUser.usuario);
                } else {
                    // Remove login inv√°lido
                    localStorage.removeItem('supabase_user');
                    console.log('üóëÔ∏è Login expirado removido');
                }
            }
        } catch (error) {
            console.error('‚ùå Erro ao verificar sess√£o:', error);
        }
    }

    /**
     * Hash simples da senha
     */
    async hashPassword(password) {
        const encoder = new TextEncoder();
        const data = encoder.encode(password + 'salt_app_2024');
        const hashBuffer = await crypto.subtle.digest('SHA-256', data);
        const hashArray = Array.from(new Uint8Array(hashBuffer));
        return hashArray.map(b => b.toString(16).padStart(2, '0')).join('');
    }

    /**
     * Cadastra novo usu√°rio
     */
    async cadastrarUsuario(usuario, senha, nomeCompleto = '') {
        try {
            const senhaHash = await this.hashPassword(senha);
            
            // Verifica se usu√°rio j√° existe
            const { data: existing } = await this.supabase
                .from('usuarios')
                .select('id')
                .eq('usuario', usuario)
                .single();

            if (existing) {
                return { success: false, error: 'Nome de usu√°rio j√° existe' };
            }

            // Cria novo usu√°rio
            const { data, error } = await this.supabase
                .from('usuarios')
                .insert({
                    usuario: usuario,
                    senha_hash: senhaHash,
                    nome_completo: nomeCompleto || usuario
                })
                .select('*')
                .single();

            if (error) {
                console.error('‚ùå Erro no cadastro:', error);
                return { success: false, error: 'Erro ao criar conta' };
            }

            console.log('‚úÖ Usu√°rio cadastrado:', data);
            return { success: true, userData: data };

        } catch (error) {
            console.error('‚ùå Erro no cadastro:', error);
            return { success: false, error: 'Erro interno' };
        }
    }

    /**
     * Faz login do usu√°rio
     */
    async logarUsuario(usuario, senha) {
        try {
            const senhaHash = await this.hashPassword(senha);
            
            // Busca usu√°rio
            const { data: userData, error } = await this.supabase
                .from('usuarios')
                .select('*')
                .eq('usuario', usuario)
                .eq('senha_hash', senhaHash)
                .single();

            if (error || !userData) {
                return { success: false, error: 'Usu√°rio ou senha incorretos' };
            }

            // Salva sess√£o localmente
            this.currentUser = userData;
            localStorage.setItem('supabase_user', JSON.stringify({
                id: userData.id,
                usuario: userData.usuario,
                nome: userData.nome_completo
            }));

            console.log('‚úÖ Login realizado:', userData.usuario);
            return { success: true, userData: userData };

        } catch (error) {
            console.error('‚ùå Erro no login:', error);
            return { success: false, error: 'Erro interno' };
        }
    }

    /**
     * Salva anota√ß√µes de uma semana (TABELA ANTIGA)
     */
    async salvarAnotacoes(semana, conteudo) {
        try {
            if (!this.currentUser) {
                return { success: false, error: 'Usu√°rio n√£o logado' };
            }

            // Verifica se j√° existe anota√ß√£o para esta semana
            const { data: existing } = await this.supabase
                .from('anotacoes')
                .select('id')
                .eq('usuario_id', this.currentUser.id)
                .eq('semana', semana)
                .single();

            let result;
            if (existing) {
                // Atualiza existente
                result = await this.supabase
                    .from('anotacoes')
                    .update({
                        conteudo: conteudo,
                        atualizado_em: new Date().toISOString()
                    })
                    .eq('id', existing.id)
                    .select('*')
                    .single();
            } else {
                // Cria nova
                result = await this.supabase
                    .from('anotacoes')
                    .insert({
                        usuario_id: this.currentUser.id,
                        semana: semana,
                        conteudo: conteudo,
                        atualizado_em: new Date().toISOString()
                    })
                    .select('*')
                    .single();
            }

            if (result.error) {
                console.error('‚ùå Erro ao salvar:', result.error);
                return { success: false, error: 'Erro ao salvar' };
            }

            console.log(`üíæ Anota√ß√µes da semana ${semana} salvas`);
            return { success: true, data: result.data };

        } catch (error) {
            console.error('‚ùå Erro ao salvar anota√ß√µes:', error);
            return { success: false, error: 'Erro interno' };
        }
    }

    /**
     * Carrega anota√ß√µes de uma semana (TABELA ANTIGA)
     */
    async carregarAnotacoes(semana) {
        try {
            if (!this.currentUser) {
                return null;
            }

            const { data, error } = await this.supabase
                .from('anotacoes')
                .select('*')
                .eq('usuario_id', this.currentUser.id)
                .eq('semana', semana)
                .single();

            if (error && error.code !== 'PGRST116') { // PGRST116 = not found
                console.error('‚ùå Erro ao carregar:', error);
                return null;
            }

            if (data) {
                console.log(`üìñ Anota√ß√µes da semana ${semana} carregadas`);
                return data.conteudo;
            }

            return null;
        } catch (error) {
            console.error('‚ùå Erro ao carregar anota√ß√µes:', error);
            return null;
        }
    }

    /**
     * Lista todas as semanas do usu√°rio (TABELA ANTIGA)
     */
    async listarSemanas() {
        try {
            if (!this.currentUser) {
                return [];
            }

            const { data, error } = await this.supabase
                .from('anotacoes')
                .select('semana, atualizado_em')
                .eq('usuario_id', this.currentUser.id)
                .order('semana', { ascending: true });

            if (error) {
                console.error('‚ùå Erro ao listar semanas:', error);
                return [];
            }

            return data || [];
        } catch (error) {
            console.error('‚ùå Erro ao listar semanas:', error);
            return [];
        }
    }

    /**
     * ===== M√âTODOS PARA RICHTEXT (NOVOS) =====
     */

    /**
     * Salva anota√ß√µes do richtext (TABELA ESPEC√çFICA)
     */
    async salvarRichtextAnotacoes(semana, conteudo) {
        try {
            if (!this.currentUser) {
                return { success: false, error: 'Usu√°rio n√£o logado' };
            }

            console.log('üíæ Salvando richtext:', {
                usuario: this.currentUser.usuario,
                semana: semana,
                tamanho: conteudo.length
            });

            // Verifica se j√° existe anota√ß√£o para esta semana
            const { data: existing } = await this.supabase
                .from('richtext_anotacoes')
                .select('id')
                .eq('usuario_id', this.currentUser.id)
                .eq('semana', semana)
                .single();

            let result;
            if (existing) {
                // Atualiza existente
                console.log('üîÑ Atualizando anota√ß√£o existente...');
                result = await this.supabase
                    .from('richtext_anotacoes')
                    .update({
                        conteudo_html: conteudo,
                        atualizado_em: new Date().toISOString()
                    })
                    .eq('id', existing.id)
                    .select('*')
                    .single();
            } else {
                // Cria nova
                console.log('‚ûï Criando nova anota√ß√£o...');
                result = await this.supabase
                    .from('richtext_anotacoes')
                    .insert({
                        usuario_id: this.currentUser.id,
                        semana: semana,
                        conteudo_html: conteudo,
                        atualizado_em: new Date().toISOString()
                    })
                    .select('*')
                    .single();
            }

            if (result.error) {
                console.error('‚ùå Erro ao salvar richtext:', result.error);
                return { success: false, error: 'Erro ao salvar', details: result.error };
            }

            console.log(`‚úÖ Richtext da semana ${semana} salvo!`);
            return { success: true, data: result.data };

        } catch (error) {
            console.error('‚ùå Erro ao salvar richtext:', error);
            return { success: false, error: 'Erro interno', details: error };
        }
    }

    /**
     * Carrega anota√ß√µes do richtext (TABELA ESPEC√çFICA)
     */
    async carregarRichtextAnotacoes(semana) {
        try {
            if (!this.currentUser) {
                return null;
            }

            const { data, error } = await this.supabase
                .from('richtext_anotacoes')
                .select('*')
                .eq('usuario_id', this.currentUser.id)
                .eq('semana', semana)
                .single();

            if (error && error.code !== 'PGRST116') { // PGRST116 = not found
                console.error('‚ùå Erro ao carregar richtext:', error);
                return null;
            }

            if (data) {
                console.log(`üìñ Richtext da semana ${semana} carregado`);
                return data.conteudo_html;
            }

            return null;
        } catch (error) {
            console.error('‚ùå Erro ao carregar richtext:', error);
            return null;
        }
    }

    /**
     * Verifica se est√° logado
     */
    isLoggedIn() {
        return this.currentUser !== null || localStorage.getItem('supabase_user') !== null;
    }

    /**
     * Pega usu√°rio atual
     */
    getCurrentUser() {
        if (this.currentUser) return this.currentUser;
        
        const saved = localStorage.getItem('supabase_user');
        if (saved) {
            try {
                return JSON.parse(saved);
            } catch (e) {
                localStorage.removeItem('supabase_user');
            }
        }
        
        return null;
    }

    /**
     * Faz logout
     */
    logout() {
        this.currentUser = null;
        localStorage.removeItem('supabase_user');
        console.log('üëã Logout realizado');
    }

    /**
     * Setup de realtime (sync autom√°tico)
     */
    setupRealtime(semana, callback) {
        if (!this.currentUser || !semana) return;

        const channel = this.supabase
            .channel(`anotacoes_${semana}`)
            .on(
                'postgres_changes',
                {
                    event: '*',
                    schema: 'public',
                    table: 'anotacoes',
                    filter: `usuario_id=eq.${this.currentUser.id} and semana=eq.${semana}`
                },
                (payload) => {
                    console.log('üîÑ Sync realtime:', payload);
                    if (callback) callback(payload);
                }
            )
            .subscribe();

        return channel;
    }
}

